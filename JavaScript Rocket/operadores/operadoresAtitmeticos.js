// Operadores Ariméticos

// Multiplicação *
console.log(10 * 3)

// Dvisão /
console.log(15 / 5)

// Soma +
console.log(5 + 5)

// Subtração -
console.log(20 - 5)

// Resto da divisão %
let remainder = 12 % 5 // o resto da divisão de 12 por 5 é 2
console.log(remainder)

// Incremento ++
let numero = 10;
console.log(++numero) // o incremento é feito antes da variável

// Decremento --
let numero1 = 10;
console.log(--numero1) // o decremento é feito antes da variável

// Exponenciaçial **
console.log(2 ** 3) // 2 elevado a 3 = 8



